#!/usr/bin/env python3
import numpy as np
"""_Multiplying with numPy_
"""


def np_matmul(mat1, mat2):
    """_summary_

    Args:
        mat1 (_type_): _matrix 1_
        mat2 (_type_): _matrix 2_

    Returns:
        _type_: _Multiplication of 2 matrices_
    """
    return np.matmul(mat1, mat2)
